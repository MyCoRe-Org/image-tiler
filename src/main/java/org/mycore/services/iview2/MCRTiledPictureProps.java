package org.mycore.services.iview2;

public class MCRTiledPictureProps {
    int countTiles;

    int width;

    int height;

    int zoomlevel;

    /**
     * @return the countTiles
     */
    public int getCountTiles() {
        return countTiles;
    }

    /**
     * @return the width
     */
    public int getWidth() {
        return width;
    }

    /**
     * @return the height
     */
    public int getHeight() {
        return height;
    }

    /**
     * @return the zoomlevel
     */
    public int getZoomlevel() {
        return zoomlevel;
    }
}